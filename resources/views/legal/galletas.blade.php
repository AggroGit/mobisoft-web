<p>En cumplimiento con el deber de información recogido en el apartado 2o del artículo 22 de la Ley 34/2002, de 11 de Julio, de Servicios de la Sociedad de la Información y del Comercio Electrónico, la presente política de cookies tiene por finalidad informarle de manera clara y precisa sobre las cookies que se utiliz an en la página web de MOBILITY SOFTWARE SOLUTIONS S.L..
¿Qué son las cookies?
Una cookie es un fichero de pequeño tamaño que los sitios web envían al navegador y se descargan en su ordenador. Las cookies permiten que la página web almacene y recupere la información sobre su visita, como su idioma preferido y otras opciones, con el fin de mejorar los servicios que se ofrecen y contribuir a tener una mejor experiencia de navegación para el usuario.
Tipos de cookies
En función de quién gestione el dominio desde donde se envían las cookies y se traten los datos, se distinguirá entre cookies propias y cookies de terceros.
Las cookies también pueden clasificarse según el plazo de tiempo que permanezcan almacenadas en el navegador del usuario, distinguiéndose entre cookies de sesión o cookies persistentes.
Finalmente, en función de la finalidad para la que se trate la información recopilada, las cookies se pueden clasificar en las siguientes categorías: Cookies técnicas: permiten al usuario navegar por un sitio web y utilizar algunos de los servicios ofrecidos por el mismo (como, por ejemplo, las que sirven para recordar los elementos integrantes de un carrito de compra).
Cookies de personalización: permiten que el usuario acceda al sitio web con unas características determinadas, como puede ser el idioma. Cookies de seguridad: sirven para impedir o dificultar los ataques contra el sitio web.
Cookies de complemento para intercambiar contenidos sociales: los llamados plug-in, que permiten compartir contenidos en redes sociales.
Cookies de análisis: permiten al responsable el seguimiento del comportamiento del usuario.
Cookies publicitarias: permiten gestionar los espacios de publicidad del sitio web.
Cookies de publicidad comportamental: como las anteriores, sirven para gestionar los espacios de publicidad del sitio web en función de la información recogida sobre el comportamiento del usuario y sus hábitos, adaptando la publicidad al perfil del usuario.
Cookies de geolocalización: utilizadas para averiguar el país dónde se encuentra el usuario.
Autorización para el uso de cookies
De conformidad con el aviso de cookies que aparece en el sitio web, el usuario puede consentir expresamente el uso de cookies que requieran consentimiento; sin embargo, el uso de cookies técnicas o necesarias no requiere de consentimiento. Sin perjuicio de todo ello, el usuario puede modificar la configuración de su navegador para rechazar el uso de las cookies.
Cómo modificar la configuración de las cookies
Usted puede restringir, bloquear o borrar las cookies de MOBILITY SOFTWARE SOLUTIONS S.L. o cualquier otra página web utilizando su navegador. En cada navegador la operativa es diferente, puede encontrar cómo hacerlo en el menú de ayuda de su navegador dónde se establece el procedimiento para su eliminación. Para más información:
